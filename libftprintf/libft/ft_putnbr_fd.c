#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char tmp;

	if (n != -2147483648)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		tmp = n % 10 + '0';
		write(fd, &tmp, 1);
	}
	else
		write(fd, "-2147483648", 11);
}
