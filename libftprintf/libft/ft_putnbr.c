#include <unistd.h>

void	ft_putnbr(int n)
{
	char tmp;

	if (n != -2147483648)
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n > 9)
			ft_putnbr(n / 10);
		tmp = (n % 10 + '0');
		write(1, &tmp, 1);
	}
	else
		write(1, "-2147483648", 11);
}
